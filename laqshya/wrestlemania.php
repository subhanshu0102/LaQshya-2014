										•	During inspection (and at any time during the event), the judges may require safety changes or other modifications to meet the harmlessness requirement. Harmful robots are either not allowed to complete at all or are later disqualified if potential harmful issues are proven or revealed in battle.<br>
										•	Survive the expected pushing, shoving, and physical rigors of competition. Suggestions may be made to avoid damage to the robot. A weak robot is usually allowed to complete at its own risk.<br>
										•	During the contest, the judge must determineif a robot’s failure was due to its own lack of durability.<br>
										•	Failures due to exposed wires or unsecured or flimsy parts shall be the responsibility of the robot with such weakness.<br>
