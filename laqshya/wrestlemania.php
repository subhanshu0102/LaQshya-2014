										•	Upon winning two sub matches, the team will be declared as winner.
										<h5>Out:</h5>
										•	A robot loses a round when any portion of the robot touches outside of the ring. It doesn’t matter if the robot falls out on its own or is pushed out.<br>
										•	The first robot touching outside of the ring loses, even if the second robot subsequently touches outside of the ring. If the referee determines that both robots touched outside of the ring at the same time the round is nullified and started over.<br>
										•	If any piece of the robot, no matter how small or even if detached, touches outside of the ring, the robot is considered out. For example, if a nut drops off a robot within the ring, the robot doesn’t immediately lose. However, if the nut is then pushed out or rolls out, the robot loses.<br>
