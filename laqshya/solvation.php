									<h3>Rules</h3>
									<p>The task of a robot in the Solvation contest is to follow a track specified by a black line or wall and traverse it between the start and finish taking the shortest time.<br> 
										1. Robot dimensions and weight.<br> 
										Weight of the robot is max 5 kg. Width of the robot is max 250 mm, height max 250 mm. The robot shall not be dangerous. These criteria will be checked before the match. Any Robot which fails in any of them will not be allowed to compete.<br> 
										2. Time <br>
										The time is measured from the referee signal to start until the moment the foremost part of the robot crosses the finish line.<br> 
										3. Time measuring <br>
										The time will be measured using an electronic gate or by a referee using a stopwatch. The time measured is final.<br> 
										4. Robot control <br>
										Since the robot crosses the starting line, it must be fully autonomous, otherwise it shall be disqualified.<br> 
										5. Restart<br>
										A contestant may not feed information on the arena to the Robot. Therefore, changing ROMs  or downloading programs is NOT allowed on the arena. However, contestants are allowed to:<br>
										:	Change switch settings (e.g. to select algorithms)<br>
										:	Replace batteries between runs<br>
										:	Adjust sensors<br>
										:	Change speed settings<br>
										:	Make repairs<br>
										With a restart at nearer flag, maximum 3 resets are allowed.<br> 
										6. Referee and organizer power <br>
										All decisions of a referee or an organizer are final.<br> 
										7. Objections<br> 
										No objections against any decision of a referee or an organizer will be admitted. Team leader may raise an objection in case of doubts concerning implementation of the rules. This objection shall be presented to the referee.<br> 
										8. Rules changes <br>
										Based on the number of participants or local conditions, the contest rules or the operating rules may be adapted by the organisers. Such changes shall be announced before the contest start and remain valid for the whole contest duration.<br>
									</p>
