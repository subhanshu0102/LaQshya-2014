										•	The right spirit of participation is expected from the participants.<br>
										•	The robots must not harm the arena in any way. If it does so, a penalty will imposed on the team. The magnitude of the penalty will be decided by referee.<br>
										•	The decision of the referee will be final and binding. <br>
										<h5> Referee Stoppage:</h5>
										•	At the referee’s discretion, the referee may choose to restart a round if: three minutes have expired; No progress has been made in some period of time; the robot fail to touch each other for some period of time; the robots are hopelessly entangled or otherwise deadlocked; both robots fail to start or both contestants signal stoppage. At the referee’s discretion, the referee may choose to end a round and choose the round winner if smoke, fire, damage, or any other violation has occurred. No problem is likely to be made even if the round is restarted.<br>
										
										
										
										
										
										<h5>Harmlessness:</h5>
